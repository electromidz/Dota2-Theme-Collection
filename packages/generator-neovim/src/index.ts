import type {
  GeneratedFile,
  GeneratorOptions,
  PlatformGenerator,
} from "@dota-themes/generator-core";
import { darken } from "@dota-themes/generator-core";
import type { HeroPalette } from "@dota-themes/palettes";

function hl(group: string, opts: string): string {
  return `vim.api.nvim_set_hl(0, "${group}", { ${opts} })`;
}

function generateLua(palette: HeroPalette): string {
  const e = palette.editor;
  const c = palette.colors;
  const name = palette.metadata.title;
  const heroSlug = palette.hero;

  const lines: string[] = [
    `-- ${name} theme for Neovim`,
    "-- Generated by dota-themes",
    `-- ${palette.metadata.description}`,
    "",
    "vim.api.nvim_command(\"highlight clear\")",
    'vim.g.colors_name = "dota-' + heroSlug + '"',
    "vim.opt.background = \"dark\"",
    "",
    "local hl = vim.api.nvim_set_hl",
    "",
    "-- Base",
    hl("Normal", `fg = "${e.foreground}", bg = "${e.background}"`),
    hl("NormalFloat", `fg = "${e.foreground}", bg = "${e.background}"`),
    hl("NormalNC", `fg = "${e.foreground}", bg = "${e.background}"`),
    "",
    "-- Cursor",
    hl("Cursor", `fg = "${e.background}", bg = "${e.cursor}"`),
    hl("CursorLine", `bg = "${e.lineHighlight}"`),
    hl("CursorColumn", `bg = "${e.lineHighlight}"`),
    "",
    "-- Selection",
    hl("Visual", `bg = "${e.selection}"`),
    hl("VisualNOS", `bg = "${e.selection}"`),
    "",
    "-- Line numbers",
    hl("LineNr", `fg = "${e.gutter}"`),
    hl("CursorLineNr", `fg = "${e.cursor}"`),
    "",
    "-- Signs & Folds",
    hl("SignColumn", `bg = "${e.background}"`),
    hl("FoldColumn", `bg = "${e.background}"`),
    hl("Folded", `bg = "${e.lineHighlight}"`),
    "",
    "-- Syntax",
    hl("Comment", `fg = "${e.comment}", italic = true`),
    "",
    hl("Keyword", `fg = "${e.keyword}"`),
    hl("Statement", `fg = "${e.keyword}"`),
    hl("Conditional", `fg = "${e.keyword}"`),
    hl("Repeat", `fg = "${e.keyword}"`),
    hl("Label", `fg = "${e.keyword}"`),
    "",
    hl("String", `fg = "${e.string}"`),
    hl("Character", `fg = "${e.string}"`),
    "",
    hl("Number", `fg = "${e.number}"`),
    hl("Float", `fg = "${e.number}"`),
    hl("Boolean", `fg = "${e.constant}"`),
    "",
    hl("Function", `fg = "${e.function}"`),
    "",
    hl("Identifier", `fg = "${e.variable}"`),
    "",
    hl("Type", `fg = "${e.type}"`),
    hl("Structure", `fg = "${e.type}"`),
    hl("Typedef", `fg = "${e.type}"`),
    "",
    hl("Constant", `fg = "${e.constant}"`),
    hl("Define", `fg = "${e.keyword}"`),
    hl("Macro", `fg = "${e.keyword}"`),
    hl("PreCondit", `fg = "${e.keyword}"`),
    hl("PreProc", `fg = "${e.keyword}"`),
    hl("Include", `fg = "${e.keyword}"`),
    "",
    hl("Operator", `fg = "${e.operator}"`),
    hl("Delimiter", `fg = "${e.punctuation}"`),
    "",
    hl("Tag", `fg = "${e.tag}"`),
    hl("Attribute", `fg = "${e.attribute}"`),
    "",
    hl("Class", `fg = "${e.class}"`),
    hl("Interface", `fg = "${e.interface}"`),
    hl("Enum", `fg = "${e.enum}"`),
    hl("EnumMember", `fg = "${e.enum}"`),
    "",
    hl("Parameter", `fg = "${e.parameter}"`),
    hl("Property", `fg = "${e.property}"`),
    "",
    hl("Builtin", `fg = "${e.builtin}"`),
    "",
    hl("Special", `fg = "${c.accent}"`),
    hl("SpecialChar", `fg = "${c.accent}"`),
    hl("SpecialKey", `fg = "${e.gutter}"`),
    "",
    "-- Diagnostics",
    hl("Error", `fg = "${c.error}"`),
    hl("WarningMsg", `fg = "${c.warning}"`),
    hl("InfoMsg", `fg = "${c.info}"`),
    hl("MoreMsg", `fg = "${c.success}"`),
    "",
    hl("Todo", `fg = "${c.accent}", bold = true`),
    hl("Search", `fg = "${e.background}", bg = "${c.accent}"`),
    hl("IncSearch", `fg = "${e.background}", bg = "${e.cursor}"`),
    "",
    "-- Completion menu",
    hl("Pmenu", `fg = "${e.foreground}", bg = "${darken(e.background, 0.05)}"`),
    hl("PmenuSel", `fg = "${e.foreground}", bg = "${e.selection}"`),
    hl("PmenuSbar", `bg = "${darken(e.background, 0.08)}"`),
    hl("PmenuThumb", `bg = "${e.comment}"`),
    "",
    "-- Statusline",
    hl("StatusLine", `fg = "${e.foreground}", bg = "${darken(e.background, 0.08)}"`),
    hl("StatusLineNC", `fg = "${e.comment}", bg = "${darken(e.background, 0.08)}"`),
    hl("WinSeparator", `fg = "${darken(e.background, -0.08)}"`),
    "",
    "-- Tabs",
    hl("TabLine", `bg = "${darken(e.background, 0.05)}"`),
    hl("TabLineFill", `bg = "${darken(e.background, 0.05)}"`),
    hl("TabLineSel", `fg = "${e.foreground}", bg = "${e.background}"`),
    "",
    "-- Misc",
    hl("Title", `fg = "${c.primary}", bold = true`),
    hl("Directory", `fg = "${c.primary}"`),
    "",
    "hl(0, \"Underlined\", { underline = true })",
    "hl(0, \"Bold\", { bold = true })",
    "hl(0, \"Italic\", { italic = true })",
    "",
    "-- Health",
    hl("HealthError", `fg = "${c.error}"`),
    hl("HealthWarning", `fg = "${c.warning}"`),
    hl("HealthSuccess", `fg = "${c.success}"`),
    "",
    "-- Diagnostic",
    hl("DiagnosticError", `fg = "${c.error}"`),
    hl("DiagnosticWarn", `fg = "${c.warning}"`),
    hl("DiagnosticInfo", `fg = "${c.info}"`),
    hl("DiagnosticHint", `fg = "${c.success}"`),
    "",
    hl("DiagnosticUnderlineError", `sp = "${c.error}", undercurl = true`),
    hl("DiagnosticUnderlineWarn", `sp = "${c.warning}", undercurl = true`),
    hl("DiagnosticUnderlineInfo", `sp = "${c.info}", undercurl = true`),
    hl("DiagnosticUnderlineHint", `sp = "${c.success}", undercurl = true`),
    "",
    "-- Diff",
    hl("DiffAdd", `bg = "${darken(c.success, 0.3)}"`),
    hl("DiffDelete", `bg = "${darken(c.error, 0.3)}"`),
    hl("DiffChange", `bg = "${darken(c.info, 0.3)}"`),
    hl("DiffText", `bg = "${darken(c.info, 0.2)}"`),
    "",
    "-- Git signs",
    hl("GitSignsAdd", `fg = "${c.success}"`),
    hl("GitSignsChange", `fg = "${c.info}"`),
    hl("GitSignsDelete", `fg = "${c.error}"`),
    "",
    "-- Telescope",
    hl("TelescopeBorder", `fg = "${e.comment}"`),
    hl("TelescopeSelectionCaret", `fg = "${c.accent}"`),
    hl("TelescopeSelection", `fg = "${e.foreground}", bg = "${e.selection}"`),
    "",
    "-- NvimTree",
    hl("NvimTreeIndentMarker", `fg = "${e.indentation}"`),
    hl("NvimTreeNormal", `fg = "${e.foreground}", bg = "${e.background}"`),
    hl("NvimTreeCursorLine", `bg = "${e.lineHighlight}"`),
    "",
    "-- WhichKey",
    hl("WhichKey", `fg = "${c.accent}"`),
    hl("WhichKeyGroup", `fg = "${c.primary}"`),
    hl("WhichKeyDesc", `fg = "${e.foreground}"`),
    "",
    "-- Lazy",
    hl("LazyButton", `bg = "${darken(e.background, 0.05)}"`),
    hl("LazyButtonActive", `bg = "${e.selection}"`),
    hl("LazyComment", `fg = "${e.comment}"`),
  ];

  return lines.join("\n");
}

export const neovimGenerator: PlatformGenerator = {
  name: "neovim",
  fileExtension: "lua",

  generate(options: GeneratorOptions): GeneratedFile[] {
    const content = generateLua(options.hero);
    const fileName = `dota-${options.hero.hero}.lua`;
    return [{ path: `${options.outputDir}/${fileName}`, content }];
  },
};
